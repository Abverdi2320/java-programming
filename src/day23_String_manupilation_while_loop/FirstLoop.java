package day23_String_manupilation_while_loop;

public class FirstLoop {
    public static void main(String[] args) {
        int i = 10;
        while (i <= 5) {
            System.out.println("Java is fun");
            i++;
        }


        int apples = 0;

        while(apples <= 3){
            System.out.println("Apple ---> " +apples );
            apples++;
        }

        System.out.println("Apples = " + apples);
    }
}
