package Repls;

import java.util.*;

public class PalindromeNumMethods {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int num = scan.nextInt();
        isPalindrome(num);



    }
    public static void isPalindrome(int num){
        boolean check = true;
        for(int i =0; i<=num; i++ ){
            if (i==num){
                System.out.println(check);
            }else{
                System.out.println(check);
            }
        }






    }
}
